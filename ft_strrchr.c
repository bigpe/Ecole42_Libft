char	*ft_strrchr(const char *src, int c)
{
	int		i;
	char	*s;

	s = (char*)src;
	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			return (char*)&s[i];
		i--;
	}
	return (0);
}
