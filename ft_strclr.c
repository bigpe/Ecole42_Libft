#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int slen;

	if (s == NULL)
		return ;
	i = 0;
	slen = ft_strlen(s);
	while (i < slen)
	{
		s[i] = '\0';
		i++;
	}
}
